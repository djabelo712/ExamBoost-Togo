// test/helpers/test_helpers.dart
// Common helpers for the ExamBoost Togo test suite.
//
// Provides:
//   - initHiveForTests() : Hive init in a temp dir + adapter registration.
//   - closeHiveAfterTests() : clean teardown.
//   - createTestUser() : AppUser with preset BKT values.
//   - createTestQuestion() : Question with preset IRT params.
//   - createTestReviewCard() : ReviewCard with preset SM-2 state.
//   - pumpAppWithProviders() : pump a widget wrapped in MultiProvider + MaterialApp.
//
// NOTE: All model adapters (QuestionAdapter, ReviewCardAdapter, AppUserAdapter,
// QuestionTypeAdapter) are generated by `dart run build_runner build` from the
// @HiveType annotations in lib/models/*.dart. Run build_runner before tests.

