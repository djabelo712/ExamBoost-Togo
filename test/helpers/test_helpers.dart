// ─── Widget pumping ────────────────────────────────────────────────

/// Pump [child] wrapped in a MaterialApp with the default mock providers.
///
/// Tests can override any provider by passing [overrides].
Future<void> pumpAppWithProviders(
  WidgetTester tester,
  Widget child, {
  AppUser? currentUser,
  SrsService? srsService,
  QuestionService? questionService,
  List<SingleChildWidget> overrides = const [],
}) async {
  final userProvider = FakeUserProvider(user: currentUser);
  await tester.pumpWidget(
    MultiProvider(
      providers: [
        ChangeNotifierProvider<UserProvider>.value(value: userProvider),
        Provider<SrsService>.value(value: srsService ?? MockSrsService()),
        Provider<QuestionService>.value(
          value: questionService ?? MockQuestionService(),
        ),
        ...overrides,
      ],
      child: MaterialApp(home: child),
    ),
  );
}
