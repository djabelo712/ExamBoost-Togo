/// - [bktMaitrise] defaults to an empty map (use [withBkt] for preset values).
/// - [niveauScolaire] defaults to '3eme'.
AppUser createTestUser({
  String id = 'test-user',
  String nom = 'Doe',
  String prenom = 'Jane',
  String niveauScolaire = '3eme',
  String? serie,
  Map<String, double>? bktMaitrise,
