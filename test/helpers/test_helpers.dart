// ─── Hive lifecycle ────────────────────────────────────────────────

/// Initialize Hive in a temp directory and register all model adapters.
/// Call this in `setUpAll` for tests that touch Hive boxes.
///
/// Safe to call multiple times: adapter registration is guarded by
/// `Hive.isAdapterRegistered`.
Directory initHiveForTests() {
  final tempDir = Directory.systemTemp.createTempSync('examboost_test_');
  Hive.init(tempDir.path);

  if (!Hive.isAdapterRegistered(0)) Hive.registerAdapter(QuestionAdapter());
  if (!Hive.isAdapterRegistered(1)) Hive.registerAdapter(QuestionTypeAdapter());
  if (!Hive.isAdapterRegistered(2)) Hive.registerAdapter(ReviewCardAdapter());
  if (!Hive.isAdapterRegistered(3)) Hive.registerAdapter(AppUserAdapter());

  return tempDir;
}

/// Close all Hive boxes and delete the temp directory.
/// Call this in `tearDownAll`.
Future<void> closeHiveAfterTests() async {
  await Hive.close();
}

